-- 横移動で行またぎを可能にする
vim.opt.whichwrap = "h,l,<,>,[,],b,s"
-- システムクリップボードと共有する
vim.opt.clipboard = "unnamedplus"
-- 検索結果をハイライト表示する
vim.opt.hlsearch = true
-- 入力中にリアルタイム検索
vim.opt.incsearch = true
-- 大文字小文字を無視
vim.opt.ignorecase = true
-- 大文字が含まれると区別する
vim.opt.smartcase = true
-- 長い行を折り返す
vim.opt.wrap = true
-- 単語単位で折り返す
vim.opt.linebreak = true